package mocent.kx.entity;

public class MocentCar {
	private int id;
	private String carVerName;
	private int carCreateBy;
	private String carCreateUserName;
	private String carCreateDate;
	private int carState;
	private int imageSizeHigh;
	private int imageSizeWidth;
	private int titleNum;
	private int lineNum;
	private int rowNum;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCarVerName() {
		return carVerName;
	}
	public void setCarVerName(String carVerName) {
		this.carVerName = carVerName;
	}
	public int getCarCreateBy() {
		return carCreateBy;
	}
	public void setCarCreateBy(int carCreateBy) {
		this.carCreateBy = carCreateBy;
	}
	
	public String getCarCreateUserName() {
		return carCreateUserName;
	}
	public void setCarCreateUserName(String carCreateUserName) {
		this.carCreateUserName = carCreateUserName;
	}
	public String getCarCreateDate() {
		return carCreateDate;
	}
	public void setCarCreateDate(String carCreateDate) {
		this.carCreateDate = carCreateDate;
	}
	public int getCarState() {
		return carState;
	}
	public void setCarState(int carState) {
		this.carState = carState;
	}
	public int getImageSizeHigh() {
		return imageSizeHigh;
	}
	public void setImageSizeHigh(int imageSizeHigh) {
		this.imageSizeHigh = imageSizeHigh;
	}
	public int getImageSizeWidth() {
		return imageSizeWidth;
	}
	public void setImageSizeWidth(int imageSizeWidth) {
		this.imageSizeWidth = imageSizeWidth;
	}
	public int getTitleNum() {
		return titleNum;
	}
	public void setTitleNum(int titleNum) {
		this.titleNum = titleNum;
	}
	public int getLineNum() {
		return lineNum;
	}
	public void setLineNum(int lineNum) {
		this.lineNum = lineNum;
	}
	public int getRowNum() {
		return rowNum;
	}
	public void setRowNum(int rowNum) {
		this.rowNum = rowNum;
	}
}
